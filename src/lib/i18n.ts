const DICTIONARY: Record<AppLanguage, TranslationDictionary> = {
  en: EN,
  ar: AR,
};

export const APP_LANGUAGES: AppLanguage[] = ["en", "ar"];

export function getDirection(language: AppLanguage): "ltr" | "rtl" {
  return language === "ar" ? "rtl" : "ltr";
}

export function translate(language: AppLanguage, key: string, values?: TranslationValues): string {
  const dictionary = DICTIONARY[language] ?? DICTIONARY.en;
  const fallbackDictionary = DICTIONARY.en;
  let text = dictionary[key] ?? fallbackDictionary[key] ?? key;

  if (!values) return text;

  for (const [token, value] of Object.entries(values)) {
    text = text.replace(new RegExp(`\\{${token}\\}`, "g"), String(value));
  }

  return text;
}
